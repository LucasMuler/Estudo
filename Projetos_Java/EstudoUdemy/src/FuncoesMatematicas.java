
public class FuncoesMatematicas {

	public static void main(String[] args) {
		
	/**
	 * Teste de Raiz quadrada
	 */
		
		int x = 4;
		
		System.out.println("a raiz quadrada de " + x + "é: " + Math.sqrt(x));
		System.out.println("a raiz quadrada de 25 é: " + Math.sqrt(25));
		
	/**
	 * Teste de potenciação 
	 */
		
		System.out.println("a potencia de " + x + " ao quadrado e: " + Math.pow(x, 2));
		System.out.println("a potencia de 2 ao quadrado e: " + Math.pow(2, 2));
		
	/**
	 * Valor absoluto
	 */
		
		
		
		System.out.println("o valor absoluto de: " + x + " e: " + Math.abs(x));
		int x1 = -4;
		System.out.println("o valor absoluto de: " + x1 + " e: " + Math.abs(x1));
	}

}
